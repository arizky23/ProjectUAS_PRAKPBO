    public void getData(){
        DefaultTableModel modelInbox = (DefaultTableModel)pesanMasukTable.getModel();
        DefaultTableModel modelOutbox = (DefaultTableModel)pesanKeluarTable.getModel();
        try {
                sql = "SELECT * FROM pesan WHERE untuk="+"'"+member.getNama()+"'";
                rs = stat.executeQuery(sql);
                modelInbox.setRowCount(0);
                while(rs.next()){                    
                    modelInbox.addRow(new Object[]{rs.getInt("id"),rs.getString("dari"),rs.getString("pesan"),rs.getString("date")});
                }
                
                sql = "SELECT * FROM pesan WHERE dari="+"'"+member.getNama()+"'";
                rs = stat.executeQuery(sql);
                modelOutbox.setRowCount(0);
                while(rs.next()){                    
                    modelOutbox.addRow(new Object[]{rs.getInt("id"),rs.getString("untuk"),rs.getString("pesan"),rs.getString("date")});
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
    }
    
