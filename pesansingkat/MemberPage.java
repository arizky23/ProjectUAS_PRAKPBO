    private void kirimBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kirimBtnActionPerformed
        // TODO add your handling code here:
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd-MM-yyyy");  
        LocalDateTime now = LocalDateTime.now();  
        String pesan = pesanField.getText();
        String untuk = penerimaField.getText();
        String dari = member.getNama();
        String date = dtf.format(now).toString();
        try{
            sql = "INSERT into pesan(dari, untuk, pesan, date) VALUES (" + "'" +dari+ "'" + "," + "'" +untuk+ "'" + "," + "'" +pesan+ "'" + "," + "'" +date+ "'"+")";
            stat.executeUpdate(sql);
            JOptionPane.showMessageDialog(this, "Pesan Berhasil Di Kirim !!!");
            getData();
            penerimaField.setText(null);
            pesanField.setText(null);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_kirimBtnActionPerformed

